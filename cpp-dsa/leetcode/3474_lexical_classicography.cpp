        // 2. Валидация 'T' - критично для сложных наложений
        for (int i = 0; i < n; ++i) {
            if (str1[i] == 'T') {
                for (int j = 0; j < m; ++j) {
                    if (word[i + j] != str2[j]) return "";
                }
            }
        }

        // 3. Быстрая проверка 'F' через string_view или кастомный memcmp
        // Но секрет 100% в том, чтобы проверять только те окна, 
        // где ПЕРВЫЙ и ПОСЛЕДНИЙ символы совпадают с str2
